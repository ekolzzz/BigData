"""
类：
1）很多事物存在 相同的属性和行为(也叫方法)，比如人有姓名年龄，能吃饭睡觉等等
2）描述 具有共同特征的事物的 抽象，称为 类 (class)

类包含两个组成部分：
    a）属性(类创建的对象有哪些数据需要保存)：比如姓名，年龄，身高，肤色等
    b）方法(类创建的对象能够干什么)：比如吃饭，睡觉，飞行，歌唱等
    
对象：
1）对象 是 类 的实例，是具体的实体


类和对象的关系：
1）类相当于图纸，对象相当于根据图纸制造的实物。类是对象的模板(不占内存空间)，对象是类的实例(占内存空间)。
2）每个对象必须有一个对应的类。
"""
