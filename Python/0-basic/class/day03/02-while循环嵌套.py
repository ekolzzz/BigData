"""
需求：跑步 5 圈，每跑步1圈，做10个俯卧撑
"""

# 跑步 1 圈
# 跑步 2 圈
# 跑步 3 圈
# 跑步 4 圈
# 跑步 5 圈

i = 1

while i <= 5 :
    print('==========================='+'\n'+'跑步%d圈' % i)
    j = 1
    while j <= 10 :
        print('做%d个俯卧撑' % j)
        j += 1
    i += 1

















