"""
组包：= 右边有多个数据时，会自动包装为元组赋值给 = 左边的变量
"""


# =号右边的数据1,2,3被自动组装成了一个元祖(1, 2, 3)，然后赋值给了=号左边的变量a
a = 1, 2, 3
# <class 'tuple'> (1, 2, 3)
print(type(a), a)


"""
拆包：多个变量 = 容器数据，当变量数量等于容器长度时，容器中的每个元素会按照次序一一赋值给 = 号左边的变量

注意：= 号左边变量的数量必须和 = 号右边容器的长度相等
"""
# 先将=号右边的元祖(1, 2, 3)拆解成数字1,2,3，然后按照顺序依次赋值给=号左边的变量a、b、c
a, b, c = (1, 2, 3)
print(a)
print(b)
print(c)

# 先组包，再拆包
a, b, c = 10, 20, 30
print(a)
print(b)
print(c)