"""
需求：跑步 5 圈，每跑步1圈，做10个俯卧撑
"""

# 跑步 1 圈
# 做 1 个俯卧撑
# 做 2 个俯卧撑
# 做 3个俯卧撑
# ...
# 做 10个俯卧撑
# 跑步 2 圈
# 跑步 3 圈
# 跑步 4 圈
# 跑步 5 圈

i = 1

# 外层循环
while i <= 5:
    print('跑步 %d 圈' % i)
    # 注意：这个 j = 1 必须写到外层循环里面
    j = 1

    # 内层循环
    while j <= 10:
        print('做 %d 个俯卧撑' % j)
        j += 1

    i += 1

















